bool IsBadPatch(int p)
{
	for(int i = 0; badPatches[i] != -1; i++)
	{
		if (p==badPatches[i]) return true;
	}
	
	return false;
}


int main(int argc, char *argv[])
{
	if (argc != 3)
	{
		printf("Usage: make_zarr_from_patches <output-zarr> <bigpatch or csv or bin>\n");
		printf("Turn patches into a surface zarr");
		return -1;
	}
		
    ZARR_c128 *outputZarr = ZARROpen_c128(argv[1]);
	
	std::vector<chunkIndex> bpChunks;
	
	BigPatch *bp = OpenBigPatch(argv[2]);
	bpChunks = GetAllPatchChunks(bp);
	
	for(auto const &i : bpChunks)
	{
		printf("Chunk: %d.%d.%d\n",std::get<2>(i),std::get<1>(i),std::get<0>(i));

		std::vector<gridPoint> gridPoints;
		ReadPatchPoints(bp,i,gridPoints);
				
		for(auto const &gp : gridPoints)
		{
			float xo = std::get<2>(gp);
			float yo = std::get<3>(gp);
			float zo = std::get<4>(gp);
			int patch = std::get<5>(gp);

            if (IsBadPatch(patch))
              continue;				
			int x = ROUND(xo);
			int y = ROUND(yo);
			int z = ROUND(zo);

			for(int xx=x-1;xx<=x+1;xx++)
			for(int yy=y-1;yy<=y+1;yy++)
			for(int zz=z-1;zz<=z+1;zz++)
			  ZARRWrite_c128(outputZarr,zz,yy,xx,255);
		}
	}	
    	
	CloseBigPatch(bp);   
	
    ZARRClose_c128(outputZarr);		
}

